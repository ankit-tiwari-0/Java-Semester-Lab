package lecture4;
//Relational Operator
// ==, !=, <, >, <= , >=

public class Relational {
    
    public static void main(String[] args){
        
      int a = 5;
      int b = 10;
      
      boolean c = (a == b);//f
      System.out.println(c);

      boolean d = (a != b);//t
      System.out.println(d);

       boolean e = (a < b);//t
       System.out.println(e);

       boolean f = (a <= b);//t ek condition correct hona chahiye
       System.out.println(f);


    }
}
