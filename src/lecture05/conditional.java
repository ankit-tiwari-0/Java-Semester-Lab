package lecture05;

public class conditional {
     public static void main(String[] args) {

        int i = 5;

        if (i == 5) {
            System.out.println("i is 5");
        } else {
            System.out.println("i is not");
        }

        int o = 6;
         
        if (o % 2 != 0) {
            
            System.out.println("o is even");
        }
        else{
            System.out.println("o is odd");
        }
    } 
}
