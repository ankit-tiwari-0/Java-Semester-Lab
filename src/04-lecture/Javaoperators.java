public class Javaoperators {
      
    public static void main(String[] args) {
        //Arithmatic operators --> +, -, *, /, %, +=, -=, /=, %=, ++, --
        int a = 5;
        int b = 10;

        int c = a+b;
        int d = a-b;
        int e = a*b;
        int f = a/b;
        int g = a%b;

        System.out.println(c + "," + d +"," + e +"," + f +"," + g  );
    }
}
