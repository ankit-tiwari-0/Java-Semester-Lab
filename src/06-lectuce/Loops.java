public static void main(String[] args) {
    int i = 10;

    // Loops ---> while

    while(i >= 1) {//doolean --> true / false

        System.out.println(i);
        i--;
    }

    int k = 0;
    while (k-- > 10) {
         System.out.println(k);
    }
   
    int m = 0;
    while (m++ < 10) {
         System.out.println(m);
    }

}